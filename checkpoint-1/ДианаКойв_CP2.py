def check_bit():  
    try:  
        # Запрашиваем значение регистра  
        register_value = int(input("Введите значение системного регистра: "))  
          
        # Запрашиваем номер бита  
        bit_number = int(input("Введите номер бита для проверки (начиная с 0): "))  
          
        # Проверяем корректность введённых данных  
        if bit_number < 0:  
            print("Ошибка: номер бита не может быть отрицательным")  
            return  
          
        # Создаём маску для проверки нужного бита  
        mask = 1 << bit_number  
          
        # Проверяем состояние бита  
        if register_value & mask:  
            print(f"Бит {bit_number} установлен (1)")  
        else:  
            print(f"Бит {bit_number} сброшен (0)")  
              
    except ValueError:  
        print("Ошибка: введите целое число")  
  
# Запуск программы  
check_bit()  